    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\brand  $brand
     * @return \Illuminate\Http\Response
     */
    public function destroy(Brand $brand)
    {
        $brand->delete();

        if ($brand) {
            return redirect('/brand')->with('success', 'Data Brand Berhasil dihapus!');
